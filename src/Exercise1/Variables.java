package Exercise1;

public class Variables {
    public static void main(String[] args) {
        //variables: container which can hold the values

        int x ; // declaration of variable
        x = 10; //initialization

        //Data types:
        //Primitive Data types:
        //-- int (large number), double (larger decimal), char, boolean
        //-- short(small number), float (small decimal) etc.
        //Derived Data types:
        //Array, ArrayList, String, HashMap etc.

        int y = 10;
        int i = x == y ? 1 : 0;
        System.out.println(i);
    }
}
