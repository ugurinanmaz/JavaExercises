package ConditinalStatements;

public class WhileLoop2_PrintOddEvenNumbers {
    public static void main(String[] args) {

        //print Even numbers
        int i = 2;
        while (i<=10){
            System.out.println(i);
            i+=2;
        }

        //print Odd numbers
        int j = 1;
        while (j<=10){
            System.out.println(j);
            j+=2;
        }


    }
}
