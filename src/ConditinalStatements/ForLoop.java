package ConditinalStatements;

public class ForLoop {

    public static void main(String[] args) {

        // If we know the exact limit of the loop we can use For-loop
        // If we Do NOT know the exact limit  we use While-Loop
        // --
        for ( int i =1 ; i<=10; i++ ){
            System.out.println(i);
        }
    }
}
